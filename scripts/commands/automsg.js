  // এখন সময় বের করি, AM/PM ফরম্যাটে
  const date = new Date();
  let hour = date.getHours();
  let minute = date.getMinutes();
  
  let ampm = hour >= 12 ? "PM" : "AM";
  hour = hour % 12;
  hour = hour ? hour : 12; // 0 কে 12 বানানো
  minute = minute < 10 ? "0" + minute : minute;

  const currentTime = `${hour}:${minute} ${ampm}`;

  if (!messages[currentTime]) return; // যদি মেসেজ না থাকে, কিছু না করো

  // মেসেজ পাঠানোর জন্য থ্রেড ID দরকার, সাধারণত গ্রুপ ID আসে
  let threadID = event.threadID;

  // বার্তা পাঠাও
